            Escapade Française vous emmène dans l’archipel français, au cœur de l’Océan Pacifique, pour échapper à la grisaille européenne.<br><br> Comme la saison s’y prête, de très nombreuses activités et événements ont lieu dans l’archipel en novembre pour vous laisser découvrir la culture et les senteurs nouvelles des îles Sous-le-Vent et des îles du Vent. Laissez-vous tenter par le Hawaiki Nui Va’a du 1er au 3 novembre, course traditionnelle de pirogue à balancier qui rassemble plus de 100 embarcations, dont l’arrivée est prévue sur la mythique plage de la pointe Matira à Bora Bora. La 17eme édition du salon du livre se déroulera du 22 au 29 novembre. Quoi de mieux pour découvrir la culture polynésienne que de se plonger dans sa littérature ? Vous pourrez assister à des conférences et expositions, rencontrer des auteurs locaux et étrangers, dans le cadre idyllique des jardins et espaces de la Maison de la Culture à Papeete.

            Enfin, l’apparition de la constellation des Pléiades (ou Matari’i ni’a) est fêtée à partir du 20 novembre. C’est la marque du passage d’une période à une autre, moment clé de la vie ancienne et marquée par de nombreux rites et rituels. De nos jours, plusieurs manifestations uniques permettent de revivre ces moments authentiques.<br><br>Notre Carnet Météo :<br>

Tandis qu’en novembre, il fait de plus en plus frais en métropole, les températures en Polynésie sont proches de la perfection : entre 27°C et 31°C pendant tout le mois. Avec beaucoup d’ensoleillement, il risque d’y avoir quelques légères pluies en journée.

Quant à la température de l’eau, la baignade est délicieuse puisque les moyennes de saison s’élèvent à 28°C.

La Polynésie française n’attend que vous !</p>

        <p>Décembre 2017 | Hèloise Melen</p>
        <br>

        <p>Strasbourg est la destination que nous privilégions en décembre.<br>

            Pendant les fêtes de fin d’année, Strasbourg, (et l’Alsace en général) se pare de ses plus belles guirlandes et lumières, et accueille un des plus beaux marchés de Noël, qui fait rêver toute l’Europe.<br><br> Escapade Française vous invite à y séjourner, pour replonger dans la magie du Noël alsacien, traditionnel, authentique et chaleureux.

            Plus de 300 chalets sont répartis sur une dizaines de sites, partout dans la ville, pour vous offrir friandises, spécialités du coin, décorations de Noël et autres souvenirs.<br>

Depuis quelques années, il y a également l’OFF de Noël, ou le marché responsable, qui donne l’opportunité de vivre les fêtes de fin d’année sous un angle de consommation différent et responsable. Le marché de Noël dure exactement un mois, entre le 24 novembre et le 24 décembre, et il est absolument incontournable.

Strasbourg et sa région ne se résument cependant pas toujours aux fêtes de Noël, c’est aussi un espace dynamique et moderne, qui accueille des activités en tout genre. Du 1er au 31 décembre, se tient le festival Premières, qui met à l’honneur de jeunes metteurs en scène européens et permet de donner un aperçu de la richesse des idées des artistes de demain.<br><br>Notre carnet météo :<br>

En décembre en Alsace, les températures sont basses, souvent comprises entre -2°C et 5°C. Elles peuvent beaucoup varier en fonction des années. Comme Strasbourg est située dans une cuvette, elle est relativement protégée du vent. L’ensoleillement est faible, et la pluviométrie comprise entre 40mm et 50mm. Il est pourtant facile d’aller se réchauffer le cœur avec un excellent vin chaud à la cannelle, en flânant dans les rues illuminées de la ville.</p>

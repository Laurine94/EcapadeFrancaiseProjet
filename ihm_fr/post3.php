        <h2>Bordeaux</h2>
        <p>5 Octobre 2017 | Hèloise Melen</p>
        <p>Le mois d’octobre se prête parfaitement à un séjour à Bordeaux.

        <br>L’Hiver s’installe doucement en France, et les premiers froids se font ressentir, surtout dans le Nord de la France. Escapade Française vous propose donc une petite virée à Bordeaux, où les températures sont encore clémentes et vous permettront de passer un merveilleux séjour au cœur des vignes. La vieille ville est un joyau de pierres ciselées, et les monuments du XVIIIème siècle tels que la Bourse ou le Grand Théâtre charmeront les amoureux d’histoire.

        En octobre, la Garonne brille au soleil matinal, et le reste de la ville offre une superbe palette de couleur, se reflétant dans le Miroir d’Eau.

        <br><br>Cet automne, le Festival international des Arts de Bordeaux Métropole (le FAB) proposera de nombreuses installations partout dans la métropole : 20 jours d’effervescence (du 5 au 25 octobre), 30 spectacles (danse, musique contemporaine, théâtre de rue,…) sur scène et hors des murs sur plus de 20 lieux.

        Les amateurs de lecture trouveront du 6 au 8 octobre la 13eme salon du livre de poche à Gradignan, village de charme situé à quelques kilomètres de Bordeaux. Vous y trouverez des salons littéraires, du théâtre, et des activités variées autour des thèmes du pouvoir et de l’imagination. Enfin, nous vous conseillons de ne pas manquer l’exceptionnelle illumination des bassins à flots du 21 octobre au soir, organisé par la compagnie Carabosse à l’occasion de la clôture de sa saison.

        Bien sûr, n’oubliez pas de gouter aux grands vins de Bordeaux, que vous trouverez dans tous les restaurants et cafés, en toute saison.<br><br>Notre Carnet Météo :<br>

        En octobre, la température à Bordeaux est encore douce : une dizaine de degrés le matin, et jusqu’à 20°C l’après-midi. Il y a peu de vent, mais souvent de la pluie, puisque Bordeaux est l’une des villes qui connaît le plus de précipitations par an en France. Cela n’altère cependant pas le charme authentique de ses quais et la qualité de son vin, alors n’hésitez plus.
        </p>

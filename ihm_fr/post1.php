        <h2>La Côte d'Azur</h2>
        <p>5 juillet 2017 | Hèloise Melen</p>
        <br>

        <p>En septembre, favoriser la Côte d'Azur. 

        <br />
        <p>Avec la fin des vacances scolaires, le mois de septembre est le moment idéal pour partir à la découverte de la Côte d'Azur dans toute son authenticité. Les plages sont vides et la région se transforme en un petit coin de paradis. Communiquer avec les habitants sera beaucoup plus simple, et ils seront en mesure de vous initier à leur accent de charme. Se promener dans les marchés et savourer les spécialités locales dans de charmants restaurants se transformera en un plaisir quotidien. En plus de toutes les activités que nous proposons tout au long de l'année, Escapade Française propose une sélection d'événements et de sorties à ne pas manquer.</p>
        <br  />
        <p>La région est un petit bijou pour les festivals de musique en plein air: jusqu'à fin septembre, vous aurez l'occasion d'écouter du jazz de qualité au festival de Cagnes-sur-Mer, tous les vendredis sur la place du château. A Saint Raphaël, entre le 15 et le 17 septembre, l'inévitable festival des quatuors à cordes se déroule avec des animations de qualité qui feront de votre voyage un événement plus que juste la côte méditerranéenne.</p>
        <br>
        <p>A Cannes, le festival du Yachting accueille de nombreux exposants internationaux qui vous feront découvrir les plaisirs nautiques; vous seriez en mesure d'admirer les derniers bateaux des plus grands chantiers navals du monde. Cannes reste fidèle à sa réputation de première foire du monde. Pour les gourmets, le festival national de gastronomie se déroule du 22 au 24 septembre. Amateurs et spécialistes notoires de la cuisine se rencontrent autour de délicieux plats cuisinés et de dégustations de produits locaux pour avoir la chance de partager des saveurs inhabituelles. Ces saveurs sont françaises et exclusivement de la région du sud.</p>
        <br>
        <br>
        </p>
        <p style="color: red;"> Notre bulletin météo: </p>
        <p>Sur la Côte d'Azur, le beau temps et les températures estivales sont au rendez-vous en septembre. On estime que le temps se situera entre 22°C et 27°C pendant la journée, et diminuera de quelques degrés le soir. La pluie est rare, mais il y a souvent du mistral, du vent, mais personnalisez l'originalité du climat provençal. Vous pouvez également vous baigner en toute sérénité en Méditerranée: les moyennes saisonnières se situent entre 20,3°C et 22,5°C.</p>

        <h2>Normandie et Bretagne</h2>
        <p>5 juillet 2017 | Hèloise Melen</p>
        <br>

        <p>Août est la saison la plus touristique de France, nous vous conseillons donc d'éviter Paris et la Côte d'Azur, où la chaleur peut être difficile à supporter.
        <br>
        <br>
        Pour échapper à la foule et à la chaleur accablante, Escapade Française vous offre l'opportunité de vous ressourcer en Normandie et en Bretagne, deux des régions les plus exceptionnelles de France avec des paysages resplendissants à cette période de l'année, sur mer comme à l'intérieur des terres.
        <br>
        <br>
        Il y a beaucoup d'activités et de monuments à admirer, ainsi que de nombreux festivals. Vous pouvez profiter des "Musicales de Normandie", un mélange de musiques du monde, de musique traditionnelle française et de musique classique. Du récital à l'orchestre, le festival se concentre sur des artistes de renommée internationale ainsi que de jeunes talents prometteurs.
        <br>
        <br>
       Si vous restez jusqu'au début du mois de septembre, ne ratez pas le Festival du film américain de Deauville, où vous aurez la chance de rencontrer de nombreuses célébrités, françaises et même hollywoodiennes selon le programme. 
       En Bretagne, la musique celtique est souvent honorée, avec des festivités telles que des bals de village où l'on peut apprendre des danses traditionnelles. Tout au long de l'été, le Festival des Arts, à la pointe du Finistère, rassemble plus de 30 000 personnes et associe l'art religieux et l'art contemporain de manière unique.
        <br>
        <br>
       <p style="color: red;"> Notre bulletin météo:</p>
        
        Que ce soit en Bretagne ou en Normandie, on ne peut jamais échapper à la pluie. Mais ne vous inquiétez pas, sans la pluie, le charme de la région ne sera pas le même! Cela fait partie de l'expérience, même si l'été a tendance à avoir moins de pluie que d'habitude.
        <br>
        <br>
        Vous aurez toujours beaucoup de soleil, avec des températures estimées entre 18°C et 25°C pendant la journée. Pour ceux qui veulent faire trempette, la température moyenne de l'eau est de 18°C à Deauville et Honfleur, alors qu'il fait plus frais à Etretat et Cherbourg (17,6°C et 17,3°C respectivement).
        <br>
        <br>
        </p>

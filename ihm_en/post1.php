        <h2>The French Riviera</h2>
        <p>july 5, 2017 | Hèloise Melen</p>
        <br>

        <p>In September, favor the French Riviera. 

        <br />
        With the school year vacation coming to an end, the month of September is the perfect time to go discover the French Riviera within all its authenticity. The beaches are empty, and the region transforms itself into a little piece of heaven. Communicating with locals will be much simpler, and they will be able to initiate you to their charming accent. Strolling through the markets and savor the local specialties in charming restaurants will turn into a daily pleasure. In addition to all the activities that we offer throughout the whole year, Escapade Française offers a selection of events and outings not to be missed. 
        <br  />
        The region is a little gem for outdoors music festivals: until the end of September, you will have the opportunity to listen to high quality jazz at the Cagnes-sur-Mer festival, every Friday in the castle square, it is ultimately an idyllic environment. In Saint Raphaël between the 15th and the 17th of September, the inevitable festival of string quartels occurs with quality animations that will make your trip extra than just the Mediterranean coast. 
        <br>
        In Cannes, the yachting festival welcomes numerous international exhibitors that will introduce you to the nautical pleasures; you would be able to admire the latest’s boats of the biggest shipyards in the world. Cannes remains faithful to its reputation as the leading fair in the world. For the gourmets, the national festival of gastronomy happens from the 22nd to the 24th of September. Amateurs and notorious specialists of culinary food meet around delicious cooked meals and tastings of local products to have the chance to share unusual flavors. Those flavors are French and exclusively from the southern region. 
        <br>
        <br>
        </p>
        <p style="color: red;"> Our weather book note: </p>
        <p>In the French Riviera, the nice weather and the summer temperatures are on point in September. It is estimated the weather will be between 22°C and 27°C during the day, and drop a few degrees in the evening.  The rain is rare, yet there is often mistral, wind, however personalize the Provence climate originality. You can also bathe with serenity in the Mediterranean Sea: the seasonal averages are between 20,3°C and 22,5°C.</p>

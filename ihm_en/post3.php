        <p>The month of October lends itself perfectly to a stay in Bordeaux. 
        <br>
        <br>
        Winter is slowly settling in France, and the first cold weather is felt, especially in the North of France. Escapade Française therefore offers you a short trip to Bordeaux, where the temperatures are still mild and will allow you to spend a wonderful stay in the heart of the vineyards. The old town is a jewel of chiselled stones, and 18th century monuments such as the Bourse or the Grand Théâtre will charm lovers of history.
        <br>
        <br>
        In October, the Garonne shines in the morning sunshine, and the rest of the city offers a superb palette of colour, reflected in the Water Mirror.
This autumn, the Festival international des Arts de Bordeaux Métropole (the FAB) will offer numerous installations throughout the city: 20 days of excitement (from 5 to 25 October), 30 shows (dance, contemporary music, street theatre,...) on stage and off the walls in more than 20 venues.
<br>
        <br>
        Lovers of reading will find from 6 to 8 October the 13th edition of the book fair in Gradignan, a charming village located a few kilometres from Bordeaux. You will find literary salons, theatre, and various activities around the themes of power and imagination. Finally, we advise you not to miss the exceptional illumination of the pools of water from October 21 in the evening, organized by the company Carabosse on the occasion of the closing of its season.
        <br>
        <br>
        Of course, don't forget to taste the great wines of Bordeaux, which you will find in all the restaurants and cafés all year round.
            <br>
        <br>
       <p style="color: red;"> Our Weather Log</p>
        
       In October, the temperature in Bordeaux is still mild: about ten degrees in the morning, and up to 20°C in the afternoon. There is little wind, but often rain, as Bordeaux is one of the cities with the most rainfall per year in France. However, this does not alter the authentic charm of its quays and the quality of its wine, so don't hesitate any longer.
